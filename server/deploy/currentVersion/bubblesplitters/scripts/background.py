class Background:
    DAY         = 0x00
    DAWN        = 0x01
    NITE        = 0x02
    game        = None
    initialized = False
    pictures    = {}
    index       = 0x3
    time        = DAY

    @staticmethod
    def createpicture(sprite_day, sprite_dawn, sprite_nite, picture_index):
        Background.pictures[picture_index] = {}
        width = 320
        height = 235
        Background.pictures[picture_index][Background.DAY ] = vpu.subsprite(sprite_day , 0, 0+(picture_index*height), width, height+(picture_index*height))
        Background.pictures[picture_index][Background.DAWN] = vpu.subsprite(sprite_dawn, 0, 0+(picture_index*height), width, height+(picture_index*height))
        Background.pictures[picture_index][Background.NITE] = vpu.subsprite(sprite_nite, 0, 0+(picture_index*height), width, height+(picture_index*height))
        
    @staticmethod
    def initialize(game):
        print("Initializing background")
        Background.game = game
        #extract_palette('day')
        #extract_palette('dawn')
        #extract_palette('nite')
        sprite_day = vpu.createsprite('bg_day')
        if not sprite_day:
            print("Cannot load sprites/bg_day.png")
            return
        sprite_dawn = vpu.createsprite('bg_dawn')
        if not sprite_dawn:
            print("Cannot load sprites/bg_dawn.png")
            return
        sprite_nite = vpu.createsprite('bg_nite')
        if not sprite_nite:
            print("Cannot load sprites/bg_nite.png")
            return
        for i in range(0,16):
            Background.createpicture(sprite_day, sprite_dawn, sprite_nite, i)
            print(f"Initialized background #{i}", end="\r")
        print("")
        vpu.deletesprite(sprite_day)
        vpu.deletesprite(sprite_dawn)
        vpu.deletesprite(sprite_nite)
        initialize = True
        
    def draw():
        vpu.select(0)
        x = (int(Background.game.width) >> 1) 
        y = (int(Background.game.height) >> 1) -5
        vpu.drawsprite(Background.pictures[Background.index][Background.time], x, y)
