class Game(BasicGame):
    
    lives = 3
    stage = 0
    world = 0
    time  = 0

    colission = None

    player = None

    @staticmethod
    def setup():

        debug("Game", "Setting up")
        Game.running = True
        Game.prepare()
               
        debug("Game", "Initializing classes")
        Game.setmap(TiledMap(Game, int(320/8),int(240/8),2))
        if not Game.map.load_tileset("blocks"):
            error("Game", "Cannot load tileset!")
        
        Game.colission = TiledMap(Game, 40,30,1)
        i = 0
        for y in range(0, Game.map.height):
            for x in range(0,  Game.map.width):
                if x==0: i+=1; continue
                if x==Game.map.width -1: i+=1; continue
                if y==Game.map.height-1: i+=1; continue
                Game.colission.setvalue(x-1,y-1, 0 if map[i] == 0x6F else 1, 0)
                i+=1
        #Game.colission.load(map_colission)

        Game.map.fill(0x6f)
        Game.map.load(map)

        Background.initialize(Game)
        Bubble.initialize(Game)
        Game.player = Player()
        Game.newgame()
        
    @staticmethod
    def newgame():
        Game.lives   = 3
        Game.stage   = 0
        Game.world   = 0
        Game.player.spawn()
        
    @staticmethod
    def loop():
        vpu.setscale(1,Game.scale, Game.scale)
        delta = 1.0
        while Game.running:
            Game.draw()
            Game.update(delta)            
            
    @staticmethod
    def update(delta):
        # do stuff
        Interpolator.update(delta)

        Game.map.update(delta)

        Game.time+=1
        if Game.time % 16==0:
            Game.map.redraw()
            
        if Game.time > 120:            
           
            for bubble in Bubble.pool:
                if bubble.enabled:
                    if int(random() * 10)==1:
                        Interpolator.add(0, 25, 60, vprint)
                        bubble.pang() 
                        Game.time = 0
                        break

        for bubble in Bubble.pool:
            if not bubble.enabled: continue
            bubble.update(delta)
        
        # required stuff
        if blackbox.ctrlc():
            debug("Game", "Control+C pressed.")
            Game.running = False
            return
            
        if joypad.menu():
            menu()
        
    @staticmethod
    def draw():
        # clear buffer
        vpu.select(1)
        vpu.fill(0,0,0,0)
        
        #draw stuff
        Game.map.x = ( Game.width   >> 1 ) - (( Game.map.width  * Game.map.tile_width  ) >> 1 )
        Game.map.y = ( Game.height  >> 1 ) - (( Game.map.height * Game.map.tile_height ) >> 1 )
        #Game.map.redraw()
        if Game.map.need_redraw:
            Background.draw()
            Game.map.draw()
        
        # raster screen and update input
        vpu.update()

def vprint(value):
    vpu.select(2)
    vpu.setcolor(255,0,0,255)
    vpu.textout(f"{value}", 200,24)