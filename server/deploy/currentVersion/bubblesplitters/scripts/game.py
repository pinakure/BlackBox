class Game(BasicGame):
    
    lives = 3
    stage = 0
    world = 0
    time  = 0

    player = None

    @staticmethod
    def setup():

        print("GAME: Setting up...")
        Game.running = True
        Game.prepare()
               
        print("GAME: Initializing classes...")
        Game.map = TiledMap(Game, int(320/8),int(240/8),2)
        if not Game.map.load_tileset("blocks"):
            print("Cannot load tileset!")
        
        
        Game.map.fill(0x6f)
        #load map...
        i=0
        for y in range(0, 30):
            for x in range(0, 40):
                Game.map.setvalue(x,y, map[i], 0)
                i+=1

        Background.initialize(Game)
        Bubble.initialize(Game)
        
        # set video scale to 2x
        vpu.setscale(0, 2.0, 2.0)
        vpu.setscale(1, 2.0, 2.0)
        
    @staticmethod
    def newgame():
        Game.lives   = 3
        Game.stage   = 0
        Game.world   = 0
        Game.player.spawn()
        
    @staticmethod
    def destroy():
        del Game.map

    @staticmethod
    def loop():
        vpu.setscale(1,Game.scale, Game.scale)
        delta = 1.0
        while Game.running:
            Game.draw()
            Game.update(delta)            
            
    @staticmethod
    def update(delta):
        # do stuff
        Interpolator.update(delta)

        Game.map.update(delta)

        Game.time+=1
        if Game.time % 16==0:
            Game.map.redraw()
            
        if Game.time > 120:            
           
            for bubble in Bubble.pool:
                if bubble.enabled:
                    if int(random() * 10)==1:
                        Interpolator.add(0, 25, 60, vprint)
                        bubble.pang() 
                        Game.time = 0
                        break

        for bubble in Bubble.pool:
            if not bubble.enabled: continue
            bubble.update(delta)
        
        # required stuff
        if blackbox.ctrlc():
            print("Control+C pressed.")
            Game.destroy()
            Game.running = False
            
        if joypad.menu():
            menu()
        
    @staticmethod
    def draw():
        # clear buffer
        vpu.select(1)
        vpu.fill(0,0,0,0)
        
        #draw stuff
        Game.map.x = ( Game.width   >> 1 ) - (( Game.map.width  * Game.map.tile_width  ) >> 1 )
        Game.map.y = ( Game.height  >> 1 ) - (( Game.map.height * Game.map.tile_height ) >> 1 )
        #Game.map.redraw()
        if Game.map.need_redraw:
            Background.draw()
            Game.map.draw()
        
        # raster screen and update input
        vpu.update()


def vprint(value):
    vpu.select(2)
    vpu.setcolor(255,0,0,255)
    vpu.textout(f"{value}", 200,24)

def setup():
    return Game.setup()

def loop():
    return Game.loop()

def destroy():
    return Game.destroy()