class Game(BasicGame):
    
    lives = 3
    stage = 0
    world = 0
    time  = 0

    colission = None

    player = None

    @staticmethod
    def setup():

        debug("Game", "Setting up")
        Game.running = True
        Game.prepare()
               
        debug("Game", "Initializing classes")
        Game.setmap(TiledMap(Game, int(320/8),int(240/8),2))
        if not Game.map.load_tileset("blocks"):
            error("Game", "Cannot load tileset!")
        Game.map.fill(0x6f)
        Game.map.load(map)
        Game.map.setsurface(0)
        Game.map.settarget(0)
        Game.map.setactive()
        Game.map.setposition(( Game.width   >> 1 ) - (( Game.map.width  * Game.map.tile_width  ) >> 1 ), ( Game.height  >> 1 ) - (( Game.map.height * Game.map.tile_height ) >> 1 ))
        
        Game.colission = TiledMap(Game, 40,30,1)
        i = 0
        for y in range(0, Game.map.height):
            for x in range(0,  Game.map.width):
                if x==0: i+=1; continue
                if x==Game.map.width -1: i+=1; continue
                if y==Game.map.height-1: i+=1; continue
                Game.colission.setvalue(x-1,y-1, 0 if map[i] == 0x6F else 1, 0)
                i+=1
        #Game.colission.load(map_colission)

        BasicGame.autoredraw = False
        Background.initialize(Game)
        Bubble.initialize(Game)
        Game.player = Player()
        Game.newgame()
        
    @staticmethod
    def newgame():
        Game.lives   = 3
        Game.stage   = 0
        Game.world   = 0
        Game.player.spawn()
        Game.map.redraw()
        
    @staticmethod
    def loop():
        vpu.setscale(1,Game.scale, Game.scale)
        delta = 1.0
        while Game.running:
            Game.draw()
            Game.update(delta)            
            
    @staticmethod
    def update(delta):
        # do stuff
        BasicGame.update(delta)
        #Interpolator.update(delta)

        #Game.map.update(delta)
    
        if joypad.a()==1:
            for bubble in Bubble.pool:
                if bubble.enabled:
                    if int(random() * 10)==1:
                        #Interpolator.add(0, 25, 60, vprint)
                        bubble.pang() 
                        BasicGame.time = 0
                        break
        
        
    @staticmethod
    def draw():
        # clear buffer
        vpu.select(1)
        vpu.fill(0,0,0,0)
        
        #Game.map.redraw()
        if Game.map.need_redraw:
            Background.draw()
            Game.map.need_redraw=False
            
        # raster screen and update input
        BasicGame.draw()

def vprint(value):
    vpu.select(2)
    vpu.setcolor(255,0,0,255)
    vpu.textout(f"{value}", 200,24)